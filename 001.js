
//Arrays
//Mutator Methods >>> These methods modify the array itself.
let cities=["London","New York","Toronto"]; // An array declared with three string items.
console.log(cities);

cities.push("Karachi","Lahore","Quetta","Peshawar")//push() method is used to add 1 or multiple elements in the last of an array.
console.log(cities);

cities.pop();// pop() method is used to delete a single item from the array, it will delete only single and last item of an array.
console.log(cities);

cities.unshift("Tokyo","Bejing","New Dehli","Tehran"); // unshifit() method is used to add 1 or multiple items at the strat of an array.
console.log(cities);

cities.shift(); // shift() method is used to remove only 1st item from the array, only 1 and only 1st.
console.log(cities);



 